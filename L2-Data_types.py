# Data types in python are classification or categorization of data items. It represents kind of value that tells what operations can be performed 
# on a particular data. Since everything is an object in Python programming, Python data types are classes and variables are instances (objects) of 
# these classes.


# Numeric - int, float, complex
# Sequence Type - string, list, tuple
# Mapping Type - dict
# Boolean - bool
# Set Type - set, frozenset
# Binary Types - bytes, bytearray, memoryview


# Numeric Data

a = 5
print(type(a))

b = 5.0
print(type(b))

c = 2 + 4j
print(type(c))

# Sequence Data Types

s = 'Welcome to the Newgen Robotics World'
print(s)

# check data type 
print(type(s))

# access string with index
print(s[1])
